int	str_comp(char *str, char *str2, int stop)
{
	int	i;

	if (stop == 0)
		return (0);
	i = 0;
	while (str[i] && str[i] == str2[i] && stop)
	{
		i++;
		stop--;
	}
	if (!stop)
		return (0);
	return (str[i] - str2[i]);
}
